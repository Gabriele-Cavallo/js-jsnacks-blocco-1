    // Faccio la media dei primi 10 numeri
sum = 0;
let avarage = 10;
for (let i = 1; i <= avarage; i++) {
    let number = i;
    sum += number;
}
console.log('sum' , sum);
let avarageNumber = sum / avarage ;
console.log('avarage-number' , avarageNumber);